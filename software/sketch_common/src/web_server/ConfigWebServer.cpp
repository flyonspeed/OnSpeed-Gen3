        s.replace("{{flap.coeff1}}", g_Config.ToString(f.AoaCurve.afCoeff[1]));
        s.replace("{{flap.coeff2}}", g_Config.ToString(f.AoaCurve.afCoeff[2]));
        s.replace("{{flap.coeff3}}", g_Config.ToString(f.AoaCurve.afCoeff[3]));

