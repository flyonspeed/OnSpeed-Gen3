    // file was open, so every line was discarded until reboot.)
    {
        const bool bWasSdLogging = g_Config.bSdLogging;
        const bool bNewSdLogging = (CfgServer.hasArg("sdLogging") &&
                                    CfgServer.arg("sdLogging") == "1");
        g_Config.bSdLogging = bNewSdLogging;

        if (bWasSdLogging != bNewSdLogging &&
            g_Config.suDataSrc.enSrc == SuDataSource::EnSensors)
        {
            // 5s timeout matches the /api/logs path. Writer holds the
            // mutex for ~100ms at a time at 208 Hz; 1s was racy under
            // sustained load. If we still can't get it, revert the in-
            // memory flag so the user's flag-state matches the actual
            // file-handle state (otherwise the box appears to be
            // logging-enabled but no file is open).
            if (xSemaphoreTake(xWriteMutex, pdMS_TO_TICKS(5000)))
            {
                if (bNewSdLogging) g_LogSensor.Open();
                else               g_LogSensor.Close();
                xSemaphoreGive(xWriteMutex);
            }
            else
            {
                g_Config.bSdLogging = bWasSdLogging;   // revert; keep state consistent
                g_Log.println(MsgLog::EnDisk, MsgLog::EnError,
                    "sdLogging toggle: xWriteMutex busy after 5s; reverted");
            }
        }
    }

    // Logging rate
    if (CfgServer.hasArg("logRate")) {
        int v = CfgServer.arg("logRate").toInt();
        // Accept only known rates; everything else collapses to 50 Hz
        // (pressure-rate, the safe default).
        const int newRate = (v == 416) ? 416 : (v == 208) ? 208 : 50;
        // Only crossing the 416 boundary requires reboot — that's the
        // case where the IMU hardware ODR changes (208<->416). The
        // 50<->208 transition keeps the IMU at 208; the producer task
        // routing flips live inside SensorReadTask/ImuReadTask on the
        // next iteration, and the log file rotates via the
        // LogFileFingerprint snapshot at the end of this handler.
        const bool bCrossesBoundary416 =
            (g_Config.iLogRate == 416) != (newRate == 416);
        if (bCrossesBoundary416)
            rebootRequired = true;
        g_Config.iLogRate = newRate;
    }

    // Aircraft parameters
    if (CfgServer.hasArg("acGrossWeight"))  g_Config.iAcGrossWeight  =CfgServer.arg("acGrossWeight").toInt();
    if (CfgServer.hasArg("acBestGlideIAS")) g_Config.fAcBestGlideIAS =CfgServer.arg("acBestGlideIAS").toFloat();
    if (CfgServer.hasArg("acVfe"))          g_Config.fAcVfe          =CfgServer.arg("acVfe").toFloat();
    // acGlimit / acNegGlimit are the active values driven by the
