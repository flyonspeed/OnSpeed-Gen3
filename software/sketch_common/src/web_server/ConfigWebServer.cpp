// Reject filenames that contain path-traversal sequences, slashes, or
// characters outside the expected set for SD-card files. Used by
// /download, /delete, /delete-bulk. Does NOT restrict by extension —
// the /logs page shows all SD-card files (logs + config backups +
// boot_log.txt + whatever the user puts there), so all of them need
// to be downloadable and deletable. Security boundary is the character
// allow-list and the .. check, which together prevent path traversal.
static bool IsSafeLogFilename(const String& s)
    {
    if (s.length() == 0 || s.length() > 32) return false;
    if (s.indexOf('/')  >= 0) return false;
    if (s.indexOf('\\') >= 0) return false;
    if (s.indexOf("..") >= 0) return false;
    for (size_t i = 0; i < s.length(); i++)
        {
        char c = s.charAt(i);
        if (!(isalnum((unsigned char)c) || c == '_' || c == '.' || c == '-')) return false;
        }
    return true;
    }

// Coredump filenames are longer than root-level log files —
// "coredump_NNNN_<firmware>_<task>.bin" easily exceeds 32 chars when
// firmware version carries dev metadata (e.g. 4.23.1-dev.45). The
// allow-list is the same as IsSafeLogFilename; only the cap differs.
// The caller prepends the /coredumps/ prefix explicitly so user input
// never appears in a path-traversal-sensitive position.
static bool IsSafeCoredumpBasename(const String& s)
    {
    if (s.length() == 0 || s.length() > 96) return false;
    if (s.indexOf('/')  >= 0) return false;
    if (s.indexOf('\\') >= 0) return false;
    if (s.indexOf("..") >= 0) return false;
    for (size_t i = 0; i < s.length(); i++)
        {
        char c = s.charAt(i);
        if (!(isalnum((unsigned char)c) || c == '_' || c == '.' || c == '-')) return false;
        }
    return true;
    }

// Returns true if `sFilename` is part of the currently-active log
// session — the .csv row stream, the paired .dbg writer log, or the
// .meta schema sidecar. Deleting any of the three while LogSensor still
// has the session open orphans the file handles and silently loses the
// matching forensic record. See ApiHandlers.cpp IsActiveLogFile() for
// the equivalent guard on the JSON delete path.
static bool IsActiveLogFile(const String& sFilename)
    {
    const char* szActiveBase = g_LogSensor.ActiveBaseName();
    if (!szActiveBase || szActiveBase[0] == '\0') return false;
    const String sBase = String(szActiveBase);
    return sFilename.equalsIgnoreCase(sBase + ".csv")
        || sFilename.equalsIgnoreCase(sBase + ".dbg")
        || sFilename.equalsIgnoreCase(sBase + ".meta");
    }

